public class Condicionais {
    public static void main(String[] args) {

        int nota = 50;

        // se nota maior ou igual a 70, então aprovado
        // if-else
        if (nota >= 70) {
            System.out.println("Aluno aprovado!");
        }else{
            System.out.println("Não aprovado.");
        }
    }
}
